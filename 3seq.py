"""
В проекте создать новый модуль 3seq.py. Задание:

Пользователь вводит элементы 1-го списка (по очереди как в МОДУЛЬ 1 или вместе как в МОДУЛЬ 2)
Затем он вводит элементы 2-го списка
Удалить из первого списка элементы присутствующие во 2-ом и вывести результат на экран
Пример работы: Введите элементы 1-го списка: 1,2,3,4,5
Введите элементы 2-го списка: 2,5
Результат: 1,3,4
"""

# Запрашиваем у пользователя ввод элементов первого списка
first_list_input = input("Введите элементы 1-го списка через запятую: ")
first_list = [x.strip() for x in first_list_input.split(',')]

# Запрашиваем у пользователя ввод элементов второго списка
second_list_input = input("Введите элементы 2-го списка через запятую: ")
second_list = [x.strip() for x in second_list_input.split(',')]

# Удаляем из первого списка элементы, которые присутствуют во втором списке
result_list = [x for x in first_list if x not in second_list]

# Выводим результат
print("Результат:", ', '.join(result_list))
