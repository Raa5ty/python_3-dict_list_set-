"""
Создать новый проект, в нем создать модуль 1seq.py. Задание:
Пользователь вводит количество элементов будущего списка
После этого по очереди по одной вводит любые цифры
Сохранить цифры в список
Отсортировать список по возрастанию и вывести на экран
Пример работы: Введите количество элементов: 3
Введите 1 элемент: 5
Введите 2 элемент: 2
Введите 3 элемент: 4
Вывод: [2, 4, 5]
"""

# Запрашиваем у пользователя количество элементов списка
while True:
    num_elements_inp = input("Введите количество элементов (не более 5): ")
    if num_elements_inp.isdigit():  # Проверяем, является ли ввод числом
        num_elements = int(num_elements_inp)
        if 1 <= num_elements <= 5:  # Проверяем, что число находится в пределах от 1 до 5
            break  # Выходим из цикла, если ввод корректен
        else:
            print("Пожалуйста, введите число от 1 до 5.")
    else:
        print("Пожалуйста, введите целое число.")

# Создаем пустой список для хранения цифр
elements = []

# По очереди запрашиваем цифры у пользователя
for i in range(num_elements):
    while True:  # Бесконечный цикл для проверки ввода
        user_input = input(f"Введите {i + 1} элемент: ")
        if user_input.isdigit():  # Проверяем, является ли ввод числом
            element = int(user_input)
            elements.append(element)
            break  # Выходим из цикла, если ввод корректен
        else:
            print("Пожалуйста, введите целое число.")  # Сообщение об ошибке

# Сортируем список по возрастанию
elements.sort()

# Выводим отсортированный список
print("Вывод:", elements)
